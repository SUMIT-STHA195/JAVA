package com.basicjava.day7;

public class SwitchStatementDemo {

	public static void main(String[] args) {
		int data=10;
		
		switch (data) {
		case 1:
			System.out.println("Inside case 1");
			break;
		case 9:
			System.out.println("Inside case 1");
			break;
		case 10:
			System.out.println("Inside case 10");
			break;
			default:
				System.out.println("Not valid input!");
		}
	}
}
